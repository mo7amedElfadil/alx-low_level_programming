#include "3-calc.h"
/**
 * op_add - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: sum of 2 ints
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 integers
 * @a: first integer
 * @b: second integer
 * Return: difference of 2 ints
 */



int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers
 * @a: first integer
 * @b: second integer
 * Return: product of 2 ints
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 integers
 * @a: first integer
 * @b: second integer
 * Return: result of division of 2 ints
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - performs modulus for 2 integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of division of 2 ints
 */

int op_mod(int a, int b)
{
	return (a % b);
}


